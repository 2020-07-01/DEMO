package javaReflect.student;

/**
 * @ClassName:  Student   
 * @Description:学生实体类
 * @author: caiji
 * @date: 2019年5月7日 上午10:50:21
 */
public class Student {
	private String name;
	private int age;
	private int id;
	private String sex;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

}
